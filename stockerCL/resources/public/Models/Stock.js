function Stock(startValue)
{
    this.Id = startValue.Id;

    this.StockExchange = startValue.StockExchange;

    this.Name = startValue.Name;

    this.IPOyear = startValue.IPOyear;

    this.Sector = startValue.Sector;

    this.Industry = startValue.Industry;

    this.SummaryQuote = startValue.SummaryQuote;
}